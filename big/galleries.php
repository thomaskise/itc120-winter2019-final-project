    <p>I currently have a client who has a kids after school ceramics program. We are working on his website redesign. One important requirement comes from the kids - they want to see their work. When they are “presenting” to their family and friends, they want to get to it fast and they want it to look good.<br /><br />

        To this end, I’m recommending a jQuery slider. “jQuery sliders offer a simple, elegant way to display content without giving up valuable real estate on your web page.(<a href="#curtziegler">Curt Zielgler</a>)”.  In the referenced article, Curt goes on to say that there are many different ways to implement these. The solution I have in mind hash images that flow along the bottom of the page and when clicked or tapped, a single image expands to the main page area. The user can either slide the main image to the previous or next, or they can go back to the thumbnail slider and quickly search through the gallery.<br /><br />

    I’ve provided some considerations in researching your need for an image gallery and an example of one solution. Don’t be intimidated, and don’t be overly concerned if your chosen format requires jQuery or Javascript. There are options out there you can download for free and very inexpensively. Many have Git repos you can access through github.com. <br /><br />

