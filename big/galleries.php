    <p>Image galleries are the way to share portfolios online. They run the gamut of designs and possibilities. Some are simple and some more complex.  They can be created simply with CSS and HTML. Feature rich galleries generally employ JavaScript and JQuery. There are a zillion formats for galleries and a zillion ways to implement each one.<br /><br />
    

    The first thing to keep in mind, I’ll call it rule #1, is that the gallery is a showcase for your images, so focus on the best presentation to suit your purpose, not the coolest gallery. Too much glitz can deter from the images that are the main feature. So be mindful. Consider the purpose of your gallery and focus your gallery shopping on the best way to present the subject matter and quality you are addressing in your current assignment.<br /><br />

    What is the purpose? Each has different needs for quality, quick access, and even given rule #1, there snazziness may play into the presentation. </p>
